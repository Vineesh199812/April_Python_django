

#list slicing

lst=[15,5,20,4,46,12,25,85,40,100,8,16]

#Negative index

#[-1 to -n]

print(lst[-1]) #last value

print(lst[-3]) #it will be in reverse order
print(lst[-6])
print(lst[-8])

print(lst[-4:-1])#lst[-4].......lst[n-1]

print(lst[-2:-5]) #it'll never execute
print(lst[-5:-2])

print(lst[-3:])# -3,-2,-1

print(lst[:-4])# begin from the starting to n-1(-4-1=-5)
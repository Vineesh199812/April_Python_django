#File Operation

#Read ===> To read data from file (r)

#write ===> To write data into file (w)

#Append ===> to update content (a)

#file_reference

#syntax

#variable_name=open("argument1","argument2")

#argument1 ===> filepath

#argument2 ===> Mode of operation

f=open("file_path","r")  #read

f=open("filepath","w") #write

f=open("filepath","a") #append
